package com.scott.net.message;
/**
 * 员工信息
 * @author scott
 *
 */
public class StaffInfoEntity {
private String staffName;
private String staffPhone;
private String staffAge;
private String staffEntryTime;
private String staffId;
private String staffIdCard;
private String staffAddress;

public String getStaffIdCard() {
	return staffIdCard;
}
public void setStaffIdCard(String staffIdCard) {
	this.staffIdCard = staffIdCard;
}
public String getStaffAddress() {
	return staffAddress;
}
public void setStaffAddress(String staffAddress) {
	this.staffAddress = staffAddress;
}
public String getStaffName() {
	return staffName;
}
public void setStaffName(String staffName) {
	this.staffName = staffName;
}
public String getStaffPhone() {
	return staffPhone;
}
public void setStaffPhone(String staffPhone) {
	this.staffPhone = staffPhone;
}
public String getStaffAge() {
	return staffAge;
}
public void setStaffAge(String staffAge) {
	this.staffAge = staffAge;
}
public String getStaffEntryTime() {
	return staffEntryTime;
}
public void setStaffEntryTime(String staffEntryTime) {
	this.staffEntryTime = staffEntryTime;
}
public String getStaffId() {
	return staffId;
}
public void setStaffId(String staffId) {
	this.staffId = staffId;
}

}
